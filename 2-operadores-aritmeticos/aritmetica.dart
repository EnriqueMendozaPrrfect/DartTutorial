// () Parentesis
// / y * División y multiplicación
// + y -	Suma y resta
// La forma en la que se resuelven es de izquierda a derecha

void main () {
  var suma = 5 + 6;
  print('el valor de la suma es: $suma');

  var resta = 5 - 2;
  print('el valor de la resta es: $resta');

  var multiplicacion = 3 * 4;
  print('el valor de la multiplicacion es: $multiplicacion');

  var divicion = 12 / 3;
  print('el valor de la divicion es: $divicion');

  var parentesis = 5 * (3 + 2);
  print('el valor de la parentesis es: $parentesis');

  parentesis = 5 + 3 * 2;
  print('el valor de la parentesis es: $parentesis');
}

